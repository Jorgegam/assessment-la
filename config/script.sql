#### ESTRUCTURA BD

DROP DATABASE IF EXISTS ASSESMENT_VENTA_COMPUTADORAS;

CREATE DATABASE ASSESMENT_VENTA_COMPUTADORAS CHARACTER SET utf8 COLLATE utf8_general_ci;

USE ASSESMENT_VENTA_COMPUTADORAS;

CREATE TABLE `CATEGORIAS` (
  `ID` INT NOT NULL AUTO_INCREMENT,
  `ID_CATEGORIA_ANID` INT NULL,
  `NOMBRE` VARCHAR(200) NOT NULL DEFAULT 'sys',
  `FECHA_CREACION` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `FECHA_ACTUALIZACION` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `USUARIO_CREACION` VARCHAR(20) NOT NULL DEFAULT 'sys',
  `USUARIO_ACTUALIZACION` VARCHAR(20) NOT NULL DEFAULT 'sys',
  PRIMARY KEY (`ID`),
  FOREIGN KEY (`ID_CATEGORIA_ANID`) REFERENCES CATEGORIAS(`ID`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8 DEFAULT COLLATE utf8_general_ci;

CREATE TABLE `PRODUCTOS` (
  `ID` INT NOT NULL AUTO_INCREMENT,
  `MODELO` VARCHAR(200) NOT NULL,
  `ID_CATEGORIA` INT NOT NULL,
  `NOMBRE` VARCHAR(200) NOT NULL,
  `ESPECIFICACIONES` TEXT NOT NULL,
  `PRECIO` decimal(15,2) NOT NULL,
  `FECHA_CREACION` timestamp NULL DEFAULT CURRENT_TIMESTAMP,
  `FECHA_ACTUALIZACION` timestamp NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `USUARIO_CREACION` VARCHAR(20) NOT NULL DEFAULT 'sys',
  `USUARIO_ACTUALIZACION` VARCHAR(20) NOT NULL DEFAULT 'sys',
  PRIMARY KEY (`ID`),
  FOREIGN KEY (`ID_CATEGORIA`) REFERENCES CATEGORIAS(`ID`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8 DEFAULT COLLATE utf8_general_ci;

CREATE TABLE `COMENTARIOS` (
  `ID` INT NOT NULL AUTO_INCREMENT,
  `ID_PRODUCTO` INT NOT NULL,
  `TITULO` VARCHAR(500) NOT NULL,
  `TEXTO` VARCHAR(250) NULL,
  `CALIFICACION` TINYINT(1) NOT NULL,
  `FECHA_CREACION` timestamp NULL DEFAULT CURRENT_TIMESTAMP,
  `FECHA_ACTUALIZACION` timestamp NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `USUARIO_CREACION` VARCHAR(20) NOT NULL DEFAULT 'sys',
  `USUARIO_ACTUALIZACION` VARCHAR(20) NOT NULL DEFAULT 'sys',
  PRIMARY KEY (`ID`),
  FOREIGN KEY (`ID_PRODUCTO`) REFERENCES PRODUCTOS(`ID`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8 DEFAULT COLLATE utf8_general_ci;

